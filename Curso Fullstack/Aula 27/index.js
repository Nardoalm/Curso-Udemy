let num = 9.3453;

// Arredonda para baixo
let floor = Math.floor(num); // 9

// Arredonda para cima
let ceil = Math.ceil(num); // 10

// Arredonda para o mais próximo 

let round = Math.round(num); // 9

// Remove a parte decimal
let trunc = Math.trunc(num); // 9

// Número aleatório entre 0 e 1
let random = Math.random(); // Ex: 0.53847...

// Valor absoluto (positivo)
let negativo = -15;
let abs = Math.abs(negativo); // 15

// Potência (base, expoente)
let potencia = Math.pow(2, 4); // 16

// Raiz quadrada
let raiz = Math.sqrt(25); // 5

// Maior valor de uma lista
let maior = Math.max(1, 5, 9, 3); // 9

// Menor valor de uma lista
let menor = Math.min(1, 5, 9, 3); // 1

// Mostrar resultados no console
console.log("Math.floor:", floor);
console.log("Math.ceil:", ceil);
console.log("Math.round:", round);
console.log("Math.trunc:", trunc);
console.log("Math.random:", random);
console.log("Math.abs:", abs);
console.log("Math.pow:", potencia);
console.log("Math.sqrt:", raiz);
console.log("Math.max:", maior);
console.log("Math.min:", menor);
